class TestScaffoldDryRun:
    """Tests for dry_run=True — no disk writes."""

    def test_dry_run_does_not_create_files(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        orch = ScaffoldOrchestrator(schema, dry_run=True)

        results = orch.run([_single_fn(funcs)])

        assert not results[0].path.exists()

    def test_dry_run_result_action_is_write(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        orch = ScaffoldOrchestrator(schema, dry_run=True)

        results = orch.run([_single_fn(funcs)])

        assert results[0].action == "write"

    def test_dry_run_still_returns_allocated_path(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        orch = ScaffoldOrchestrator(schema, dry_run=True)

        results = orch.run([_single_fn(funcs, verb="create")])

        assert results[0].path.name.endswith("_create.sql")

    def test_dry_run_with_override_still_skips(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        overrides = tmp_path / "overrides"
        (overrides / "functions").mkdir(parents=True)
        (overrides / "functions" / "00001_create.sql").write_text("-- mine")

        orch = ScaffoldOrchestrator(schema, overrides_dir=overrides, dry_run=True)
        results = orch.run([_single_fn(funcs, "create")])

        assert results[0].action == "skip"
