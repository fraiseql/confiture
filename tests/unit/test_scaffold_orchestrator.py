class TestScaffoldOverride:
    """Tests for the override-skip mechanism."""

    def test_skips_when_override_exists(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        overrides = tmp_path / "overrides"
        overrides_funcs = overrides / "functions"
        overrides_funcs.mkdir(parents=True)
        # Pre-create the override for the would-be first allocation
        (overrides_funcs / "00001_create.sql").write_text("-- my override")

        orch = ScaffoldOrchestrator(schema, overrides_dir=overrides)
        results = orch.run([_single_fn(funcs, "create")])

        assert results[0].action == "skip"

    def test_override_does_not_write_to_schema(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        overrides = tmp_path / "overrides"
        overrides_funcs = overrides / "functions"
        overrides_funcs.mkdir(parents=True)
        (overrides_funcs / "00001_create.sql").write_text("-- my override")

        orch = ScaffoldOrchestrator(schema, overrides_dir=overrides)
        results = orch.run([_single_fn(funcs, "create")])

        assert not results[0].path.exists()

    def test_no_overrides_dir_never_skips(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        orch = ScaffoldOrchestrator(schema, overrides_dir=None)

        results = orch.run([_single_fn(funcs, "create")])

        assert results[0].action == "write"

    def test_missing_overrides_dir_never_skips(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        overrides = tmp_path / "overrides_does_not_exist"
        orch = ScaffoldOrchestrator(schema, overrides_dir=overrides)

        results = orch.run([_single_fn(funcs, "create")])

        assert results[0].action == "write"

    def test_partial_override_only_skips_matching(self, tmp_path: Path) -> None:
        schema, funcs = _make_env(tmp_path)
        overrides = tmp_path / "overrides"
        (overrides / "functions").mkdir(parents=True)
        # Only first function has an override
        (overrides / "functions" / "00001_create.sql").write_text("-- mine")

        orch = ScaffoldOrchestrator(schema, overrides_dir=overrides)
        results = orch.run([_single_fn(funcs, "create"), _single_fn(funcs, "update")])

        actions = {r.path.name.split("_", 1)[1]: r.action for r in results}
        assert actions["create.sql"] == "skip"
        assert actions["update.sql"] == "write"


